const DICT: Record<Locale, Record<string, string>> = { de, fr, it, en };

type TranslateParams = Record<string, string | number>;

interface I18nContextValue {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string, params?: TranslateParams) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function normalizeLocale(value: string | null | undefined): Locale {
  return value && (LOCALES as string[]).includes(value) ? (value as Locale) : 'de';
}

export function translate(locale: Locale, key: string, params?: TranslateParams): string {
  let s = DICT[locale]?.[key] ?? DICT.de[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.split(`{${k}}`).join(String(v));
    }
  }
  return s;
}

/** Wählt aus einem i18n-JSON-Feld {de,fr,it,en} die aktive Sprache mit DE-Fallback. */
export function localized(
  field: Record<string, string> | undefined | null,
  locale: Locale,
): string {
  if (!field) return '';
  return field[locale] ?? field.de ?? Object.values(field)[0] ?? '';
}

export function LocaleProvider({
  initialLocale = 'de',
  children,
}: {
  initialLocale?: Locale;
  children: React.ReactNode;
}) {
  const [locale, setLocaleState] = useState<Locale>(initialLocale);

  useEffect(() => {
    const stored = normalizeLocale(localStorage.getItem('kh-locale'));
    setLocaleState(stored);
    document.documentElement.setAttribute('lang', stored);
  }, []);

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    localStorage.setItem('kh-locale', l);
    document.documentElement.setAttribute('lang', l);
  }, []);

  const t = useCallback(
    (key: string, params?: TranslateParams) => translate(locale, key, params),
    [locale],
  );

  return <I18nContext.Provider value={{ locale, setLocale, t }}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    return {
      locale: 'de',
      setLocale: () => {},
      t: (k: string, p?: TranslateParams) => translate('de', k, p),
    };
  }
  return ctx;
}
