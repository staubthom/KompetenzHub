#!/usr/bin/env node
/**
 * Generiert die Web-Registrierung der Plugins automatisch aus deren Manifesten.
 *
 * Warum: Next.js/Turbopack bündelt zur Build-Zeit. `next/dynamic(() => import('<paket>'))`
 * braucht statische Import-Pfade – darum muss IRGENDEIN Build-Artefakt die Plugins
 * aufzählen. Statt diese Liste von Hand zu pflegen (apps/web/src/plugins/registry.ts),
 * leitet dieses Skript alles aus den Plugin-Manifesten (plugins/packages) ab.
 *
 * Erzeugt:
 *   - apps/web/src/plugins/registry.generated.ts        (pluginWebRegistry)
 *   - apps/web/src/plugins/transpile-packages.generated.json  (für next.config.mjs)
 *
 * Konvention (verbindlich): Der `component`-Name im Manifest entspricht dem Dateinamen
 * unter `<plugin>/web/<Component>.tsx`. Fehlt eine referenzierte Datei, bricht das Skript
 * mit Fehler ab (Tippfehler werden so früh erkannt).
 *
 * Läuft automatisch via `predev`/`prebuild` von apps/web – manuell: `npm run generate:plugins`.
 */
import { readFileSync, writeFileSync, existsSync, readdirSync, statSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');
const packagesDir = join(repoRoot, 'plugins', 'packages');
const outDir = join(repoRoot, 'apps', 'web', 'src', 'plugins');
const LOCALES = ['de', 'fr', 'it', 'en'];

/** Liest ein JSON-File. */
function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'));
}

/** Sammelt alle Plugin-Pakete mit gültigem Manifest. */
function discoverPlugins() {
  if (!existsSync(packagesDir)) return [];
  const plugins = [];
  for (const entry of readdirSync(packagesDir).sort()) {
    const dir = join(packagesDir, entry);
    if (!statSync(dir).isDirectory()) continue;
    const manifestPath = join(dir, 'manifest.json');
    const pkgPath = join(dir, 'package.json');
    if (!existsSync(manifestPath) || !existsSync(pkgPath)) continue;

    const manifest = readJson(manifestPath);
    const pkg = readJson(pkgPath);
    const pluginId = manifest.pluginId;
    const packageName = pkg.name;
    const contributions = manifest.contributions ?? {};

    // Referenzierte Web-Komponenten sammeln (pages/widgets/actions/tabs).
    const referenced = new Set();
    for (const p of contributions.pages ?? []) referenced.add(p.component);
    for (const w of contributions.widgets ?? []) referenced.add(w.component);
    for (const a of contributions.actions ?? []) referenced.add(a.component);
    for (const t of contributions.tabs ?? []) referenced.add(t.component);

    // Existenz der Komponenten-Dateien prüfen (Konvention: web/<Component>.tsx).
    for (const comp of referenced) {
      const file = join(dir, 'web', `${comp}.tsx`);
      if (!existsSync(file)) {
        throw new Error(
          `[generate-plugin-registry] Plugin "${pluginId}": Komponente "${comp}" referenziert, ` +
            `aber Datei fehlt: ${file}`,
        );
      }
    }

    // Vorhandene i18n-Locales bestimmen.
    const locales = LOCALES.filter((l) => existsSync(join(dir, 'i18n', `${l}.json`)));

    plugins.push({ pluginId, packageName, contributions, locales });
  }
  return plugins;
}

/** Erzeugt den TypeScript-Quelltext der generierten Registry. */
function renderRegistry(plugins) {
  const lines = [];
  lines.push('/* eslint-disable */');
  lines.push('// @generated by scripts/generate-plugin-registry.mjs — NICHT von Hand bearbeiten.');
  lines.push('// Quelle: plugins/packages/*/manifest.json. Neu erzeugen: npm run generate:plugins');
  lines.push('');
  lines.push("import dynamic from 'next/dynamic';");
  lines.push("import type { PluginComponentProps, PluginWebModule } from './registry-types';");

  // i18n-Importe.
  for (const p of plugins) {
    for (const l of p.locales) {
      lines.push(`import ${ident(p.pluginId)}_${l} from '${p.packageName}/i18n/${l}.json';`);
    }
  }
  lines.push('');
  lines.push('export const pluginWebRegistry: Record<string, PluginWebModule> = {');
  for (const p of plugins) {
    lines.push(`  ${jsonKey(p.pluginId)}: {`);
    lines.push(`    pages: {${renderPages(p)}},`);
    lines.push(`    widgets: {${renderWidgets(p)}},`);
    lines.push(`    components: {${renderComponents(p)}},`);
    lines.push(`    translations: {${renderTranslations(p)}},`);
    lines.push('  },');
  }
  lines.push('};');
  lines.push('');
  return lines.join('\n');
}

function dyn(packageName, component) {
  return `dynamic<PluginComponentProps>(() => import('${packageName}/web/${component}'), { ssr: false })`;
}

function renderPages(p) {
  const pages = p.contributions.pages ?? [];
  if (pages.length === 0) return '';
  const entries = pages.map(
    (pg) => `\n      ${jsonKey(pg.route)}: ${dyn(p.packageName, pg.component)},`,
  );
  return `${entries.join('')}\n    `;
}

function renderWidgets(p) {
  const widgets = p.contributions.widgets ?? [];
  if (widgets.length === 0) return '';
  const bySlot = new Map();
  for (const w of widgets) {
    const arr = bySlot.get(w.slot) ?? [];
    arr.push(w.component);
    bySlot.set(w.slot, arr);
  }
  const entries = [...bySlot.entries()].map(([slot, comps]) => {
    const items = comps.map((c) => dyn(p.packageName, c)).join(', ');
    return `\n      ${jsonKey(slot)}: [${items}],`;
  });
  return `${entries.join('')}\n    `;
}

function renderComponents(p) {
  // Action- und Tab-Komponenten werden namentlich referenziert.
  const names = new Set();
  for (const a of p.contributions.actions ?? []) names.add(a.component);
  for (const t of p.contributions.tabs ?? []) names.add(t.component);
  if (names.size === 0) return '';
  const entries = [...names].map((c) => `\n      ${jsonKey(c)}: ${dyn(p.packageName, c)},`);
  return `${entries.join('')}\n    `;
}

function renderTranslations(p) {
  if (p.locales.length === 0) return '';
  const entries = p.locales.map((l) => `\n      ${l}: ${ident(p.pluginId)}_${l},`);
  return `${entries.join('')}\n    `;
}

/** Sicherer JS-Identifier-Teil aus einer pluginId (z. B. "my-plugin" → "my_plugin"). */
function ident(pluginId) {
  return pluginId.replace(/[^a-zA-Z0-9_]/g, '_');
}

/** Objekt-Schlüssel: einfache Bezeichner ohne Quotes, sonst in Quotes. */
function jsonKey(key) {
  return /^[a-zA-Z_][a-zA-Z0-9_]*$/.test(key) ? key : `'${key}'`;
}

function main() {
  const plugins = discoverPlugins();
  const registry = renderRegistry(plugins);
  writeFileSync(join(outDir, 'registry.generated.ts'), registry, 'utf8');

  const transpile = plugins.map((p) => p.packageName).sort();
  writeFileSync(
    join(outDir, 'transpile-packages.generated.json'),
    JSON.stringify(transpile, null, 2) + '\n',
    'utf8',
  );

  const ids = plugins.map((p) => p.pluginId).join(', ') || '(keine)';
  console.log(`[generate-plugin-registry] ${plugins.length} Plugin(s): ${ids}`);
}

main();
